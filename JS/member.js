// Event listeners and modal functionality
document.addEventListener('DOMContentLoaded', function() {
    const tabs = document.querySelectorAll('.team-tab');
    const contents = document.querySelectorAll('.team-content');
    const hamburger = document.querySelector('.hamburger');
    const nav = document.querySelector('nav');

    // Hamburger menu toggle
    hamburger.addEventListener('click', function() {
        this.classList.toggle('active');
        nav.classList.toggle('active');
    });

    // Close menu when clicking a nav link
    const navLinks = document.querySelectorAll('nav ul li a');
    navLinks.forEach(link => {
        link.addEventListener('click', function() {
            hamburger.classList.remove('active');
            nav.classList.remove('active');
        });
    });

    // Tab switching functionality
    tabs.forEach(tab => {
        tab.addEventListener('click', function() {
            tabs.forEach(t => t.classList.remove('active'));
            this.classList.add('active');
            
            contents.forEach(content => content.classList.remove('active'));
            
            const teamId = this.getAttribute('data-team');
            document.getElementById(teamId).classList.add('active');
        });
    });
    
    // Add event listeners to all "VIEW PROFILE" buttons
    const profileButtons = document.querySelectorAll('.view-profile');
    profileButtons.forEach(button => {
        button.addEventListener('click', function(e) {
            e.preventDefault();
            
            // Get member name from the card
            const memberName = this.closest('.member-card').querySelector('.member-name').textContent;
            
            // Open modal with detailed member info
            openMemberModal(memberName);
        });
    });
});

